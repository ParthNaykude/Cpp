//c++-data-types-practice
#include <iostream>
#include <string>
using namespace std;

int main() 
{

    int integerNumber = 10;
    char character = 'A';
    long longNumber = 1234567890;
    float floatNumber = 3.14;
    double doubleNumber = 3.14159;
    bool booleanValue = true;
    string stringValue = "Hello World";
    
    cout << "Integer:" << integerNumber << endl;
    cout << "Character: " << character << endl;
    cout << "Long: " << longNumber << endl;
    cout << "Float: " << floatNumber << endl;
    cout << "Double: " << doubleNumber << endl;
    cout << "Boolean: " << booleanValue << endl;
    cout << "String: " << stringValue << endl;
    
    return 0;
}
